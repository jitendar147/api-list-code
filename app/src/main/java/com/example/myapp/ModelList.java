package com.example.myapp;

public class ModelList {

    private String id;
    private String imageView;
    private String fullSizeImageView;
    private String description;
    private String personImage;
    private String personImageFullSize;
    private String personName;
    private String personLocation;
    private String personBio;


    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getImageView() {
        return imageView;
    }

    public void setImageView(String imageView) {
        this.imageView = imageView;
    }

    public String getFullSizeImageView() {
        return fullSizeImageView;
    }

    public void setFullSizeImageView(String fullSizeImageView) {
        this.fullSizeImageView = fullSizeImageView;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getPersonImage() {
        return personImage;
    }

    public void setPersonImage(String personImage) {
        this.personImage = personImage;
    }

    public String getPersonImageFullSize() {
        return personImageFullSize;
    }

    public void setPersonImageFullSize(String personImageFullSize) {
        this.personImageFullSize = personImageFullSize;
    }

    public String getPersonName() {
        return personName;
    }

    public void setPersonName(String personName) {
        this.personName = personName;
    }

    public String getPersonBio() {
        return personBio;
    }

    public void setPersonBio(String personBio) {
        this.personBio = personBio;
    }

    public String getPersonLocation() {
        return personLocation;
    }

    public void setPersonLocation(String personLocation) {
        this.personLocation = personLocation;
    }
}
